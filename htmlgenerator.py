def generate_website(title, description, palette_name):
    """
    Main pipeline that generates the full website
    """

    # 1️⃣ Get selected color palette
    palette = palette_generator.get_palette(palette_name)

    # 2️⃣ Generate CSS using palette
    css = generate_css(palette)

    # 3️⃣ Generate HTML structure using builder
    html_structure = html_builder.build_full_page(title, description)

    # 4️⃣ Generate AI content using Granite
    ai_prompt = f"""
Create modern website content for a page.

Title: {title}

Description:
{description}

Write short professional website text content for sections like
hero, features, about, testimonials and contact.
"""

    ai_content = generate_ai_html(ai_prompt)

    # 5️⃣ Combine CSS and HTML into a single document
    final_html = html_structure.replace(
        '<link rel="stylesheet" href="styles.css">',
        f"<style>\n{css}\n</style>"
    )

    # 6️⃣ Insert AI content comment (optional for future expansion)
    final_html += f"\n<!-- AI Generated Content -->\n<!-- {ai_content[:500]} -->"

    # 7️⃣ Save HTML file
    file_path = "generated_website.html"

    with open(file_path, "w", encoding="utf-8") as f:
        f.write(final_html)

    return final_html, file_path


