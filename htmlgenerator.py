"""AI HTML Generator"""

def generate_ai_html(prompt):
    """
    Generate HTML content using IBM Granite model
    """

    # Tokenize prompt
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)

    # Generate output
    outputs = model.generate(
        **inputs,
        max_new_tokens=600,
        temperature=0.4,
        top_p=0.9,
        do_sample=True
    )

    # Decode result
    generated_text = tokenizer.decode(outputs[0], skip_special_tokens=True)

    return generated_text

