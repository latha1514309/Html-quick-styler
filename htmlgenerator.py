class HTMLPageBuilder:
    """
    Responsible for building the HTML structure of the website
    """

    def __init__(self):
        self.available_sections = [
            "hero",
            "features",
            "about",
            "portfolio",
            "testimonials",
            "pricing",
            "contact",
            "footer"
        ]


    # ------------------------------------------------------
    # Detect sections based on description keywords
    # ------------------------------------------------------
    def detect_sections(self, description):

        description = description.lower()
        detected = []

        for section in self.available_sections:
            if section in description:
                detected.append(section)

        # Ensure footer always exists
        if "footer" not in detected:
            detected.append("footer")

        return detected


    # ------------------------------------------------------
    # Generate SEO Meta Tags
    # ------------------------------------------------------
    def generate_meta_tags(self, title, description):

        meta = f"""
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<meta name="description" content="{description}">
<meta name="keywords" content="website, portfolio, modern site">
<meta name="author" content="HTML Quick Styler AI">
<title>{title}</title>
"""
        return meta


