"""Website Generator Pipeline"""

def generate_website(template_name, palette_name, inputs):

    # ---------------------------------
    # 1 Get selected palette
    # ---------------------------------
    palette = palette_engine.get_palette(palette_name)

    css = generate_css(palette)

    # ---------------------------------
    # 2 Generate AI content
    # ---------------------------------
    prompt = f"""
Generate short professional website text.

Template: {template_name}

User Data:
{inputs}

Write:
Hero section text
About section text
Short description text
"""

    ai_text = generate_content(prompt)

    # ---------------------------------
    # 3 Prepare template data
    # ---------------------------------

    data = {}

    if template_name == "Portfolio Website":

        data = {
            "name": inputs[0],
            "hero_text": ai_text,
            "about": inputs[1],
            "skills": inputs[2],
            "projects": inputs[3],
            "email": inputs[4]
        }

        html = html_templates.portfolio(data)


    elif template_name == "SaaS Landing Page":

        data = {
            "product": inputs[0],
            "hero_text": ai_text,
            "feature1": inputs[2],
            "feature2": inputs[3],
            "pricing": inputs[4]
        }

        html = html_templates.saas(data)


    elif template_name == "Corporate Website":

        data = {
            "company": inputs[0],
            "mission": inputs[1],
            "services": inputs[2],
            "team": inputs[3],
            "contact": inputs[4]
        }

        html = html_templates.corporate(data)


    elif template_name == "Restaurant Website":

        data = {
            "restaurant": inputs[0],
            "hero_text": ai_text,
            "about": inputs[1],
            "dish": inputs[2],
            "location": inputs[3],
            "phone": inputs[4]
        }

        html = html_templates.restaurant(data)


    elif template_name == "Personal Blog":

        data = {
            "blog": inputs[0],
            "about": inputs[1],
            "post": inputs[3],
            "post_text": ai_text,
            "email": inputs[4]
        }

        html = html_templates.blog(data)

    # ---------------------------------
    # 4 Insert CSS
    # ---------------------------------

    final_html = html.replace(
        "<head>",
        f"<head>\n{css}"
    )

    # ---------------------------------
    # 5 Save HTML file
    # ---------------------------------

    file_path = "generated_website.html"

    with open(file_path, "w", encoding="utf-8") as f:
        f.write(final_html)

    return final_html, file_path


